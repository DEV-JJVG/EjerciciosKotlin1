fun main() {
    val primerNumero: Int = 48
    val segundoNumero: Int = 18
    val resultado = maximoComunDivisor(primerNumero,segundoNumero)
    println("El máximo común divisor: $resultado")
}
fun maximoComunDivisor(primerNumero : Int, segundoNumero: Int): Int{
     var x = primerNumero
    var y = segundoNumero
    while(y != 0){  
        val resto = x % y
        x = y
        y = resto
        
    }  
    return x
} 